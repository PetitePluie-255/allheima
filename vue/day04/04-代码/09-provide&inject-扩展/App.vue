<template>
  <div class="app">
    我是APP组件
    <button @click="change">修改数据</button>
    <SonA></SonA>
    <SonB></SonB>
  </div>
</template>

<script>
import SonA from './components/SonA.vue'
import SonB from './components/SonB.vue'
export default {
  // 1. 祖先组件上通过provide提供数据 - 提供者
  // 优势: 所有的后代组件都可以通过inject进行获取
  provide() {
    return {
      // 基本数据类型 - 非响应式
      color: this.color,
      // 复杂数据类型 - 响应式
      userInfo: this.userInfo
    }
  },
  data() {
    return {
      color: 'pink',
      userInfo: {
        name: 'zs',
        age: 18,
      },
    }
  },
  methods: {
    change() {
      this.color = 'red'
      this.userInfo.name = 'ls'
    },
  },
  components: {
    SonA,
    SonB,
  },
}
</script>

